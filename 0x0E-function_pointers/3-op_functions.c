#include "3-calc.h"

/**
 * op_add - Adds two ints
 * @a: First int
 * @b: Second int
 *
 * Return: Sum of both ints
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts two ints
 * @a: First int
 * @b: Second int
 *
 * Return: Difference of both ints
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two ints
 * @a: First int
 * @b: Second int
 *
 * Return: Product of both ints
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two ints
 * @a: First int
 * @b: Second int
 *
 * Return: Quotient of both ints
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulo two ints
 * @a: First int
 * @b: Second int
 *
 * Return: Remainder of both ints
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
